    function appendCollisionExplanation(lines, frame, profile) {
      if (!frame.displayStep || frame.displayStep.action !== "fold" || !profile) {
        lines.push("当前帧红线：无折弯碰撞判定。");
        return;
      }
      const isFoldCompletionState = frame.drawStep > 0 && frame.drawStep % 2 === 0 && !frame.showingFlipCompletionState;
      const compareWorkingFlapArea = !isFoldCompletionState && !frame.showingFlipCompletionState;
      const details = collisionSegmentDetails(
        profile,
        frame.displayStep,
        isFoldCompletionState,
        compareWorkingFlapArea,
        frame
      );
      if (details.length === 0) {
        lines.push("当前帧红线：无硬碰撞命中。");
        return;
      }
      details.slice(0, 4).forEach(detail => {
        lines.push(`红线段 ${detail.index}-${detail.index + 1}：${detail.reason.detail}`);
      });
      if (details.length > 4) {
        lines.push(`红线段还有 ${details.length - 4} 处，未在列表中展开。`);
      }
      if (dom.showDecisionPoints.value === "true") {
        lines.push("判定点：用于确认点列判定口径。后续正式红线规则应以已成型折弯点命中碰撞区为主，再把该点直接相邻的两条线段加入红线集合。");
      }
    }

    function rebuild() {
      try {
        dom.errorBox.style.display = "none";
        const order = {
          width: Number(dom.widthInput.value),
          colorDown: dom.colorDownInput.value === "true",
          points: JSON.parse(dom.flatProfileInput.value).map(normalizePoint),
          steps: JSON.parse(dom.stepsInput.value).map(normalizeStep)
        };

        if (!Number.isFinite(order.width) || order.width <= 0) {
          throw new Error("板宽 Width 必须是正数");
        }
        if (order.points.length < 2) {
          throw new Error("平板点列至少需要 2 个点");
        }

        const frames = buildFrames(order);
        state = {
          order,
          frames,
          viewBounds: getFixedViewBounds(order, frames)
        };

        dom.frameSelect.innerHTML = "";
        state.frames.forEach((frame, index) => {
          const option = document.createElement("option");
          option.value = String(index);
          option.textContent = `${frame.title} | ${frame.kind} | ${frame.displayStep ? frame.displayStep.name : "初始态"}`;
          dom.frameSelect.appendChild(option);
        });
        dom.frameSelect.value = "0";

        draw();
      } catch (error) {
        dom.errorBox.textContent = error.message;
        dom.errorBox.style.display = "block";
      }
    }

