    function normalizePoint(point, index) {
      const x = Number(point.x);
      const y = Number(point.y);
      if (!Number.isFinite(x) || !Number.isFinite(y)) {
        throw new Error(`Invalid flat point at index ${index + 1}`);
      }
      return { x, y };
    }

    function normalizeStep(step, index) {
      const action = String(step.action || "fold").toLowerCase();
      if (!["fold", "flip"].includes(action)) {
        throw new Error(`步骤 ${index + 1} 的 action 只支持 "fold" 或 "flip"`);
      }
      const anchorIndex = Number(step.anchorIndex);
      const innerOuter = Number(step.innerOuter);
      const direction = Number(step.direction);
      const angle = Number(step.angle || 0);
      const backGauge = Number(step.backGauge || 0);
      if (!Number.isInteger(anchorIndex)) {
        throw new Error(`步骤 ${index + 1} 的 anchorIndex 必须是整数`);
      }
      if (![0, 1].includes(innerOuter)) {
        throw new Error(`步骤 ${index + 1} 的 innerOuter 只能是 0 或 1`);
      }
      if (![0, 1].includes(direction)) {
        throw new Error(`步骤 ${index + 1} 的 direction 只能是 0 或 1`);
      }
      return {
        action,
        name: step.name || `${action.toUpperCase()} ${index + 1}`,
        anchorIndex,
        innerOuter,
        direction,
        angle,
        backGauge,
        springback: Number(step.springback || 0),
        grip: Number(step.grip || 0),
        releaseHeight: Number(step.releaseHeight || 0),
        retract: Number(step.retract || 0),
        regrip: Number(step.regrip || 0)
      };
    }

    function formatNumber(value) {
      return Math.round(value * 10) / 10;
    }

    function formatPoint(point) {
      return `(${formatNumber(point.x)}, ${formatNumber(point.y)})`;
    }

    function rotatePoint(pivot, point, angleDeg) {
      const rad = angleDeg * Math.PI / 180;
      const dx = point.x - pivot.x;
      const dy = point.y - pivot.y;
      return {
        x: pivot.x + dx * Math.cos(rad) - dy * Math.sin(rad),
        y: pivot.y + dx * Math.sin(rad) + dy * Math.cos(rad)
      };
    }

    function interpolateProfiles(from, to, t) {
      if (from.length !== to.length) {
        return deepClone(to);
      }
      return from.map((point, index) => ({
        x: point.x + (to[index].x - point.x) * t,
        y: point.y + (to[index].y - point.y) * t
      }));
    }

    function getFlipPivot(order) {
      return { x: -order.width / 2, y: 0 };
    }

    function shouldImplicitFlip(steps, index) {
      if (index < 0 || index >= steps.length - 1) {
        return false;
      }
      if (steps[index].action !== "fold") {
        return false;
      }
      return steps[index].innerOuter !== steps[index + 1].innerOuter;
    }

    function shouldApplyFlipAfterStep(steps, index, appliedStepCount, applyFlipAfterLastIncludedStep) {
      if (index < 0 || index >= steps.length) {
        return false;
      }
      if (steps[index].action === "flip") {
        return true;
      }
      if (!shouldImplicitFlip(steps, index)) {
        return false;
      }
      const isTrailingAppliedStep = index === appliedStepCount - 1;
      return !isTrailingAppliedStep || applyFlipAfterLastIncludedStep;
    }

    function applyFoldStep(profile, step) {
      const next = deepClone(profile);
      const pivotIndex = Math.max(0, Math.min(step.anchorIndex, next.length - 1));
      const pivot = next[pivotIndex];
      const rotateLowerIndexSide = step.innerOuter === 0;
      const rotateAngle = step.direction === 0 ? 180 + Math.abs(step.angle) : 180 - Math.abs(step.angle);
      if (rotateLowerIndexSide) {
        for (let i = 0; i < pivotIndex; i++) {
          next[i] = rotatePoint(pivot, next[i], rotateAngle);
        }
      } else {
        for (let i = pivotIndex + 1; i < next.length; i++) {
          next[i] = rotatePoint(pivot, next[i], rotateAngle);
        }
      }
      return next;
    }

    function buildStageProfile(order, appliedStepCount, applyFlipAfterLastIncludedStep) {
      let profile = deepClone(order.points);
      for (let i = 0; i < appliedStepCount; i++) {
        const step = order.steps[i];
        if (step.action === "fold") {
          profile = applyFoldStep(profile, step);
        }
        if (shouldApplyFlipAfterStep(order.steps, i, appliedStepCount, applyFlipAfterLastIncludedStep)) {
          const pivot = getFlipPivot(order);
          profile = profile.map(point => rotatePoint(pivot, point, 180));
        }
      }
      return profile;
    }

    function resolveColorDown(order, appliedStepCount, applyFlipAfterLastIncludedStep) {
      let colorDown = order.colorDown;
      for (let i = 0; i < appliedStepCount; i++) {
        if (shouldApplyFlipAfterStep(order.steps, i, appliedStepCount, applyFlipAfterLastIncludedStep)) {
          colorDown = !colorDown;
        }
      }
      return colorDown;
    }

