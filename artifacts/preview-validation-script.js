    function drawProfile(view, profile, frame) {
      if (profile.length < 2) {
        return;
      }

      ctx.save();
      ctx.strokeStyle = getComputedStyle(document.documentElement).getPropertyValue("--line").trim();
      ctx.lineWidth = 5;
      ctx.lineJoin = "round";
      ctx.lineCap = "round";
      ctx.beginPath();
      profile.forEach((point, index) => {
        const p = view(point);
        if (index === 0) {
          ctx.moveTo(p.x, p.y);
        } else {
          ctx.lineTo(p.x, p.y);
        }
      });
      ctx.stroke();

      if (dom.showColorLine.value === "true") {
        ctx.strokeStyle = getComputedStyle(document.documentElement).getPropertyValue("--accent-2").trim();
        ctx.lineWidth = 2;
        const offset = frame.colorDown ? 10 : -10;
        ctx.beginPath();
        profile.forEach((point, index) => {
          const p = view({ x: point.x, y: point.y + offset / 3 });
          if (index === 0) {
            ctx.moveTo(p.x, p.y);
          } else {
            ctx.lineTo(p.x, p.y);
          }
        });
        ctx.stroke();
      }

      if (dom.showAnchor.value === "true" && frame.displayStep) {
        const pivot = profile[Math.max(0, Math.min(frame.displayStep.anchorIndex, profile.length - 1))];
        const p = view(pivot);
        ctx.fillStyle = getComputedStyle(document.documentElement).getPropertyValue("--accent").trim();
        ctx.beginPath();
        ctx.arc(p.x, p.y, 7, 0, Math.PI * 2);
        ctx.fill();
        ctx.font = "12px Microsoft YaHei UI";
        ctx.fillText("支点", p.x + 10, p.y - 10);
      }

      ctx.restore();
    }

