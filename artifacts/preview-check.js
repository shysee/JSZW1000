    function buildDecisionPoints(profile, frame) {
      const step = frame.displayStep;
      const isFoldCompletionState = frame.drawStep > 0 && frame.drawStep % 2 === 0 && !frame.showingFlipCompletionState;
      const compareWorkingFlapArea = !isFoldCompletionState && !frame.showingFlipCompletionState;
      const points = [
        { name: "Feed area probe", point: { x: -160, y: 40 } },
        { name: "Upper clamp outer probe", point: { x: -160, y: 190 } },
        { name: "Feed plane lower probe", point: { x: -120, y: -20 } },
        { name: "Lower fold outer probe", point: { x: -150, y: -140 } },
        { name: "Working flap probe", point: getWorkingFlapProbePoint(step, profile, frame.kind, frame) }
      ];

      const currentIndex = getCurrentFoldSegmentIndex(step, profile);
      if (currentIndex >= 0 && currentIndex < profile.length - 1) {
        points.push({
          name: "Current fold segment midpoint",
          point: segmentMidpoint(profile, currentIndex),
          segmentIndex: currentIndex,
          currentFoldSegment: true
        });
      }

      const formedIndex = getFirstFormedProbeSegmentIndex(step, profile);
      if (formedIndex >= 0 && formedIndex < profile.length - 1) {
        points.push({
          name: "Formed segment midpoint",
          point: segmentMidpoint(profile, formedIndex),
          segmentIndex: formedIndex
        });
      }

      return points.map(item => ({
        ...item,
        feedReason: feedCompletionViolationReason(item.point, step, compareWorkingFlapArea, profile, frame),
        foldReason: foldCompletionViolationReason(item.point, step, profile),
        activeReason: isFoldCompletionState
          ? foldCompletionViolationReason(item.point, step, profile)
          : feedCompletionViolationReason(item.point, step, compareWorkingFlapArea, profile, frame)
      }));
    }

    function getWorkingFlapProbePoint(step, profile = null, frameKind = null, frame = null) {
      const lowerFlap = previewUsesLowerWorkingFlap(step, profile, frameKind, frame);
      const basis = flapBasis(lowerFlap);
      return {
        x: basis.ux * 220 + collisionBoundary.workingFlapWidth / 2,
        y: basis.uy * 220
      };
    }

    function getCurrentFoldSegmentIndex(step, profile) {
      const anchorIndex = Math.max(0, Math.min(step.anchorIndex, profile.length - 1));
      return step.innerOuter === 0 ? anchorIndex - 1 : anchorIndex;
    }

    function getFirstFormedProbeSegmentIndex(step, profile) {
      const currentIndex = getCurrentFoldSegmentIndex(step, profile);
      for (let index = 0; index < profile.length - 1; index += 1) {
        if (index === currentIndex) {
          continue;
        }
        if (isFormedAngle(profile, index) || isFormedAngle(profile, index + 1)) {
          return index;
        }
      }
      return -1;
    }

    function collisionSegmentIndices(profile, step, isFoldCompletionState, compareWorkingFlapArea, frame = null) {
      return new Set(collisionSegmentDetails(profile, step, isFoldCompletionState, compareWorkingFlapArea, frame).map(detail => detail.index));
    }

    function collisionSegmentDetails(profile, step, isFoldCompletionState, compareWorkingFlapArea, frame = null) {
      const details = [];
      const added = new Set();
      const addDetail = (index, pointIndex, collisionPoint, reason) => {
        if (index < 0 || index >= profile.length - 1 || added.has(index)) {
          return;
        }
        if (isCurrentFoldSegment(index, step, profile)) {
          return;
        }

        added.add(index);
        details.push({
          index,
          pointIndex,
          midpoint: collisionPoint,
          reason,
          label: reason.shortText
        });
      };

      const anchorIndex = Math.max(0, Math.min(step.anchorIndex, profile.length - 1));
      const rightSideUsesLowerIndices = step.innerOuter === 0;
      const start = rightSideUsesLowerIndices ? anchorIndex - 1 : anchorIndex + 1;
      const end = rightSideUsesLowerIndices ? 1 : profile.length - 2;
      const increment = rightSideUsesLowerIndices ? -1 : 1;

      for (let i = start; rightSideUsesLowerIndices ? i >= end : i <= end; i += increment) {
        if (!isFormedAngle(profile, i) || isClampFoldPoint(profile[i])) {
          continue;
        }
        const reason = isFoldCompletionState
          ? foldCompletionViolationReason(profile[i], step, profile)
          : feedCompletionViolationReason(profile[i], step, compareWorkingFlapArea, profile, frame);
        if (!reason) {
          continue;
        }
        addDetail(i - 1, i, profile[i], reason);
        addDetail(i, i, profile[i], reason);
      }

      return details;
    }

    function segmentMidpoint(profile, index) {
      return {
        x: (profile[index].x + profile[index + 1].x) / 2,
        y: (profile[index].y + profile[index + 1].y) / 2
      };
    }

    function addSegmentIfBodyViolates(hits, profile, index, step, isFoldCompletionState, compareWorkingFlapArea) {
      if (index < 0 || index >= profile.length - 1) {
        return;
      }
      if (isCurrentFoldSegment(index, step, profile)) {
        return;
      }
      const midpoint = {
        x: (profile[index].x + profile[index + 1].x) / 2,
        y: (profile[index].y + profile[index + 1].y) / 2
      };
      if (isClampFoldPoint(midpoint)) {
        return;
      }
      const violates = isFoldCompletionState
        ? pointViolatesFoldCompletionBoundary(midpoint)
        : pointViolatesFeedCompletionBoundary(midpoint, step, compareWorkingFlapArea);
      if (violates) {
        hits.add(index);
      }
    }

    function isCurrentFoldSegment(index, step, profile) {
      const anchorIndex = Math.max(0, Math.min(step.anchorIndex, profile.length - 1));
      return step.innerOuter === 0
        ? index === anchorIndex - 1
        : index === anchorIndex;
    }

    function isFormedAngle(profile, index) {
      if (index <= 0 || index >= profile.length - 1) {
        return false;
      }
      const previous = profile[index - 1];
      const current = profile[index];
      const next = profile[index + 1];
      const v1x = previous.x - current.x;
      const v1y = previous.y - current.y;
      const v2x = next.x - current.x;
      const v2y = next.y - current.y;
      const length1 = Math.hypot(v1x, v1y);
      const length2 = Math.hypot(v2x, v2y);
      if (length1 < 0.001 || length2 < 0.001) {
        return false;
      }
      const cross = Math.abs(v1x * v2y - v1y * v2x);
      const dot = v1x * v2x + v1y * v2y;
      const angle = Math.atan2(cross, dot) * 180 / Math.PI;
      return angle > 1 && angle < 179;
    }

    function isClampFoldPoint(point) {
      return Math.abs(point.x) <= 0.001 && Math.abs(point.y) <= 0.001;
    }

    function pointViolatesFeedCompletionBoundary(point, step, compareWorkingFlapArea) {
      if (point.x > 0.001) {
        return false;
      }
      return Boolean(feedCompletionViolationReason(point, step, compareWorkingFlapArea));
    }

    function pointViolatesFoldCompletionBoundary(point) {
      if (point.x > 0.001) {
        return false;
      }
      return Boolean(foldCompletionViolationReason(point));
    }

    function feedCompletionViolationReason(point, step, compareWorkingFlapArea, profile = null, frame = null) {
      const clampHit = upperClampHit(point);
      if (clampHit) {
        return clampHit;
      }
      if (!compareWorkingFlapArea) {
        return null;
      }
      const flapHit = previewFlapHit(point, step, profile, "送料 / 待折态", frame);
      if (flapHit) {
        return {
          shortText: flapHit.label + "干涉",
          detail: "送料 / 待折态判定点 " + formatPoint(point) + " 落入" + flapHit.label + "判定区。"
        };
      }
      return null;
    }

    function foldCompletionViolationReason(point, step, profile = null) {
      const clampHit = upperClampHit(point);
      if (clampHit) {
        return clampHit;
      }
      const flapHit = previewFlapHit(point, step, profile, "折弯完成态", null);
      if (flapHit) {
        return {
          shortText: flapHit.label + "干涉",
          detail: "折弯完成态判定点 " + formatPoint(point) + " 落入" + flapHit.label + "判定区。"
        };
      }
      return null;
    }

    function upperClampHit(point) {
      if (pointInPolygon(point, clipPolygonAboveYZero(upperClampBandPoints(-collisionBoundary.flapLength)))) {
        return {
          shortText: "上钳口边界碰撞",
          detail: "判定点 " + formatPoint(point) + " 落入上钳口判定区。"
        };
      }
      return null;
    }

    function upperFoldBoundaryY(x) {
      return -Math.tan(collisionBoundary.topBoundaryAngle * Math.PI / 180) * x;
    }

    function upperInternalBoundaryY(x) {
      return upperFoldBoundaryY(x) - collisionBoundary.upperClampVerticalOffset;
    }

    function lowerFoldBoundaryY(x) {
      return Math.tan(collisionBoundary.bottomBoundaryAngle * Math.PI / 180) * x;
    }

    function isOutsideBoundaryBand(y, a, b) {
      return y < Math.min(a, b) - 0.001 || y > Math.max(a, b) + 0.001;
    }

    function pointInPolygon(point, polygon) {
      let inside = false;
      for (let i = 0, j = polygon.length - 1; i < polygon.length; j = i, i += 1) {
        const a = polygon[i];
        const b = polygon[j];
        const intersects = ((a.y > point.y) !== (b.y > point.y))
          && point.x < (b.x - a.x) * (point.y - a.y) / ((b.y - a.y) || 0.000001) + a.x;
        if (intersects) {
          inside = !inside;
        }
      }
      return inside;
    }

    function pointIntrudesPreviewFlaps(point, step, profile = null, frameKind = null, frame = null) {
      return Boolean(previewFlapHit(point, step, profile, frameKind, frame));
    }

    function previewFlapHit(point, step, profile = null, frameKind = null, frame = null) {
      const workingLowerFlap = previewUsesLowerWorkingFlap(step, profile, frameKind, frame);
      if (pointIntrudesFlapArea(point, workingLowerFlap, { x: 0, y: 0 })) {
        return { label: workingLowerFlap ? "下折弯翻板" : "上折弯翻板" };
      }
      const parkedLowerFlap = !workingLowerFlap;
      if (pointIntrudesFlapArea(point, parkedLowerFlap, parkedFlapOrigin(step, parkedLowerFlap))) {
        return { label: parkedLowerFlap ? "停靠下翻板" : "停靠上翻板" };
      }
      return null;
    }

    function pointIntrudesFlapArea(point, lowerFlap, origin) {
      return pointInPolygon(point, flapAreaPoints(lowerFlap, origin));
    }

    function resolveDisplayedDirection(profile, step) {
      if (!step || !profile || profile.length < 2) {
        return step?.direction ?? 0;
      }

      const pivotIndex = Math.max(0, Math.min(step.anchorIndex, profile.length - 1));
      const foldedIndices = [];
      if (step.innerOuter === 0) {
        for (let index = 0; index < pivotIndex; index += 1) {
          foldedIndices.push(index);
        }
      } else {
        for (let index = pivotIndex + 1; index < profile.length; index += 1) {
          foldedIndices.push(index);
        }
      }

      if (!foldedIndices.length) {
        return step.direction;
      }

      const pivotY = profile[pivotIndex].y;
      const averageY = foldedIndices.reduce((sum, index) => sum + profile[index].y, 0) / foldedIndices.length;
      return averageY >= pivotY ? 0 : 1;
    }

    function resolveNextFoldDirection(frame, step, profile = null) {
      if (frame && state && Array.isArray(state.frames)) {
        const nextFrame = state.frames[frame.index + 1];
        const nextIsFoldCompletion = nextFrame && nextFrame.drawStep > 0 && nextFrame.drawStep % 2 === 0 && !nextFrame.showingFlipCompletionState;
        if (nextIsFoldCompletion && nextFrame.displayStep) {
          const nextProfile = getDisplayProfile(state.order, state.frames, frame.index + 1);
          return resolveDisplayedDirection(nextProfile, nextFrame.displayStep);
        }
      }
      return step?.direction ?? resolveDisplayedDirection(profile, step);
    }

    function previewUsesLowerWorkingFlap(step, profile = null, frameKind = null, frame = null) {
      const isFeedPhase = frame && frame.drawStep > 0 && frame.drawStep % 2 === 1 && !frame.showingFlipCompletionState;
      const direction = isFeedPhase
        ? resolveNextFoldDirection(frame, step, profile)
        : resolveDisplayedDirection(profile, step);
      return direction === 0;
    }

    function resolveFrameDirection(frame, profile = null) {
      if (!frame || !frame.displayStep) {
        return 0;
      }
      const isFeedPhase = frame.drawStep > 0 && frame.drawStep % 2 === 1 && !frame.showingFlipCompletionState;
      return isFeedPhase
        ? resolveNextFoldDirection(frame, frame.displayStep, profile)
        : resolveDisplayedDirection(profile, frame.displayStep);
    }

    function parkedFlapOrigin(step, lowerFlap) {
      const retreat = Math.max(1, Math.min(4, Number(step.retract || 0) + 1)) * collisionBoundary.parkedFlapRetreatUnit;
      const angle = lowerFlap
        ? 180 + collisionBoundary.bottomBoundaryAngle
        : 180 - collisionBoundary.topBoundaryAngle;
      const rad = angle * Math.PI / 180;
      return {
        x: retreat * Math.cos(rad),
        y: retreat * Math.sin(rad)
      };
    }

    function flapAreaPoints(lowerFlap, origin) {
      const basis = flapBasis(lowerFlap);
      const width = collisionBoundary.workingFlapWidth;
      const endAlong = collisionBoundary.flapLength;
      const frontStart = { x: origin.x, y: origin.y };
      const frontEnd = { x: origin.x + width, y: origin.y };
      return [
        frontStart,
        { x: frontStart.x + endAlong * basis.ux, y: frontStart.y + endAlong * basis.uy },
        { x: frontEnd.x + endAlong * basis.ux, y: frontEnd.y + endAlong * basis.uy },
        frontEnd
      ];
    }

    function flapBasis(lowerFlap) {
      const angle = lowerFlap
        ? 180 + collisionBoundary.lowerFlapAngle
        : 180 - collisionBoundary.upperFlapAngle;
      const rad = angle * Math.PI / 180;
      const ux = Math.cos(rad);
      const uy = Math.sin(rad);
      return {
        ux,
        uy,
        nx: lowerFlap ? -uy : uy,
        ny: lowerFlap ? ux : -ux
      };
    }

