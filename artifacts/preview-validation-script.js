    function combinedCollisionZonePolygons(step, profile, frame) {
      const selectedIds = selectedCollisionRegionIds(step, profile, frame);
      return collisionRegionCandidates(step, profile, frame)
        .filter(region => selectedIds.has(region.id))
        .map(region => region.polygon);
    }

    function selectedCollisionRegionIds(step, profile, frame) {
      if (!step || step.action !== "fold") {
        return new Set();
      }

      const workingLowerFlap = previewUsesLowerWorkingFlap(step, profile, frame?.kind, frame);
      return new Set(collisionRegionCandidates(step, profile, frame)
        .filter(region => isSelectedCollisionRegion(region, workingLowerFlap))
        .map(region => region.id));
    }

    function isSelectedCollisionRegion(region, workingLowerFlap) {
      const sides = region.sides;
      return workingLowerFlap
        ? sides.lowerFlap === "L" && (sides.upperClamp === "L" || sides.upperFlap === "L")
        : sides.upperFlap === "L" && (sides.lowerClamp === "L" || sides.lowerFlap === "L");
    }

    function collisionRegionCandidates(step, profile, frame) {
      if (!step || step.action !== "fold") {
        return [];
      }

      const clipRect = {
        minX: -collisionBoundary.flapLength,
        maxX: collisionBoundary.workingFlapWidth,
        minY: -collisionBoundary.flapLength,
        maxY: collisionBoundary.flapLength
      };
      const workingLowerFlap = previewUsesLowerWorkingFlap(step, profile, frame?.kind, frame);
      const upperFlapOrigin = workingLowerFlap ? parkedFlapOrigin(step, false) : { x: 0, y: 0 };
      const lowerFlapOrigin = workingLowerFlap ? { x: 0, y: 0 } : parkedFlapOrigin(step, true);
      const boundaryLines = [
        { key: "upperClamp", line: clampBoundaryLine(true, clipRect.minX) },
        { key: "lowerClamp", line: clampBoundaryLine(false, clipRect.minX) },
        { key: "upperFlap", line: flapOuterEdgeLine(flapAreaPoints(false, upperFlapOrigin)) },
        { key: "lowerFlap", line: flapOuterEdgeLine(flapAreaPoints(true, lowerFlapOrigin)) },
        { key: "horizontalTangent", line: horizontalTangentLine(clipRect) }
      ];

      const regions = [];
      const combinations = [];
      const count = boundaryLines.length;
      for (let mask = 0; mask < (1 << count); mask += 1) {
        combinations.push(mask);
      }
      combinations.forEach(mask => {
        let polygon = rectPolygon(clipRect);
        const sides = {};
        boundaryLines.forEach((item, index) => {
          const keepLeft = ((mask >> index) & 1) === 1;
          sides[item.key] = keepLeft ? "L" : "R";
          polygon = keepLeft
            ? clipPolygonByVisualLeftOfLine(polygon, item.line)
            : clipPolygonByVisualRightOfLine(polygon, item.line);
        });
        polygon = clipPolygonToRect(polygon, clipRect);
        if (polygonArea(polygon) >= 1) {
          regions.push({
            id: regions.length + 1,
            sides,
            polygon
          });
        }
      });
      return regions;
    }

    function horizontalTangentLine(clipRect) {
      return {
        a: { x: clipRect.minX, y: 0 },
        b: { x: clipRect.maxX, y: 0 }
      };
    }

    function polygonArea(points) {
      let area = 0;
      for (let i = 0, j = points.length - 1; i < points.length; j = i, i += 1) {
        area += (points[j].x * points[i].y) - (points[i].x * points[j].y);
      }
      return Math.abs(area) / 2;
    }

    function polygonCentroid(points) {
      if (!points.length) {
        return { x: 0, y: 0 };
      }
      const sum = points.reduce((acc, point) => ({
        x: acc.x + point.x,
        y: acc.y + point.y
      }), { x: 0, y: 0 });
      return {
        x: sum.x / points.length,
        y: sum.y / points.length
      };
    }

    function rectPolygon(rect) {
      return [
        { x: rect.minX, y: rect.minY },
        { x: rect.maxX, y: rect.minY },
        { x: rect.maxX, y: rect.maxY },
        { x: rect.minX, y: rect.maxY }
      ];
    }

    function clampBoundaryLine(upFold, leftX) {
      return upFold
        ? { a: { x: 0, y: 0 }, b: { x: leftX, y: upperFoldBoundaryY(leftX) } }
        : { a: { x: 0, y: 0 }, b: { x: leftX, y: lowerFoldBoundaryY(leftX) } };
    }

    function flapOuterEdgeLine(flapPolygon) {
      return {
        a: flapPolygon[3],
        b: flapPolygon[2]
      };
    }

    function clipPolygonByVisualLeftOfLine(points, line) {
      return clipPolygonByBoundary(
        points,
        point => isOnVisualLeftOfLine(point, line),
        (a, b) => intersectSegmentWithLine(a, b, line)
      );
    }

    function clipPolygonByVisualRightOfLine(points, line) {
      return clipPolygonByBoundary(
        points,
        point => !isOnVisualLeftOfLine(point, line) || pointOnInfiniteLine(point, line),
        (a, b) => intersectSegmentWithLine(a, b, line)
      );
    }

    function isOnVisualLeftOfLine(point, line) {
      const dy = line.b.y - line.a.y;
      if (Math.abs(dy) < 0.001) {
        return point.x <= Math.max(line.a.x, line.b.x) + 0.001;
      }
      const t = (point.y - line.a.y) / dy;
      const lineX = line.a.x + (line.b.x - line.a.x) * t;
      return point.x <= lineX + 0.001;
    }

    function pointOnInfiniteLine(point, line) {
      return Math.abs(lineSide(line, point)) <= 0.001;
    }

    function lineSide(line, point) {
      return (line.b.x - line.a.x) * (point.y - line.a.y)
        - (line.b.y - line.a.y) * (point.x - line.a.x);
    }

    function intersectSegmentWithLine(a, b, line) {
      const segmentX = b.x - a.x;
      const segmentY = b.y - a.y;
      const lineX = line.b.x - line.a.x;
      const lineY = line.b.y - line.a.y;
      const denominator = segmentX * lineY - segmentY * lineX;
      if (Math.abs(denominator) < 0.001) {
        return { x: a.x, y: a.y };
      }
      const t = ((line.a.x - a.x) * lineY - (line.a.y - a.y) * lineX) / denominator;
      return {
        x: a.x + t * segmentX,
        y: a.y + t * segmentY
      };
    }

    function clipPolygonBelowYZero(points) {
      const clipped = [];
      for (let i = 0; i < points.length; i += 1) {
        const current = points[i];
        const previous = points[(i + points.length - 1) % points.length];
        const currentInside = current.y <= 0.001;
        const previousInside = previous.y <= 0.001;
        if (currentInside !== previousInside) {
          clipped.push(intersectSegmentWithY(previous, current, 0));
        }
        if (currentInside) {
          clipped.push(current);
        }
      }
      return clipped;
    }

    function uniquePoints(points) {
      const seen = new Set();
      const result = [];
      points.forEach(point => {
        const key = `${Math.round(point.x * 1000)}:${Math.round(point.y * 1000)}`;
        if (!seen.has(key)) {
          seen.add(key);
          result.push(point);
        }
      });
      return result;
    }

    function convexHull(points) {
      if (points.length <= 2) {
        return points.slice();
      }

      const sorted = points.slice().sort((a, b) => a.x === b.x ? a.y - b.y : a.x - b.x);
      const cross = (origin, a, b) => (a.x - origin.x) * (b.y - origin.y) - (a.y - origin.y) * (b.x - origin.x);
      const lower = [];
      sorted.forEach(point => {
        while (lower.length >= 2 && cross(lower[lower.length - 2], lower[lower.length - 1], point) <= 0) {
          lower.pop();
        }
        lower.push(point);
      });
      const upper = [];
      for (let index = sorted.length - 1; index >= 0; index -= 1) {
        const point = sorted[index];
        while (upper.length >= 2 && cross(upper[upper.length - 2], upper[upper.length - 1], point) <= 0) {
          upper.pop();
        }
        upper.push(point);
      }
      lower.pop();
      upper.pop();
      return lower.concat(upper);
    }

    function clipPolygonToRect(points, rect) {
      let clipped = points;
      clipped = clipPolygonByBoundary(clipped, point => point.x >= rect.minX - 0.001, (a, b) => intersectSegmentWithX(a, b, rect.minX));
      clipped = clipPolygonByBoundary(clipped, point => point.x <= rect.maxX + 0.001, (a, b) => intersectSegmentWithX(a, b, rect.maxX));
      clipped = clipPolygonByBoundary(clipped, point => point.y >= rect.minY - 0.001, (a, b) => intersectSegmentWithY(a, b, rect.minY));
      clipped = clipPolygonByBoundary(clipped, point => point.y <= rect.maxY + 0.001, (a, b) => intersectSegmentWithY(a, b, rect.maxY));
      return removeAdjacentDuplicatePoints(clipped);
    }

    function removeAdjacentDuplicatePoints(points) {
      const result = [];
      points.forEach(point => {
        const previous = result[result.length - 1];
        if (!previous || Math.hypot(point.x - previous.x, point.y - previous.y) > 0.001) {
          result.push(point);
        }
      });
      if (result.length > 1) {
        const first = result[0];
        const last = result[result.length - 1];
        if (Math.hypot(first.x - last.x, first.y - last.y) <= 0.001) {
          result.pop();
        }
      }
      return result;
    }

    function clipPolygonByBoundary(points, isInside, intersect) {
      if (!points.length) {
        return [];
      }

      const clipped = [];
      for (let i = 0; i < points.length; i += 1) {
        const current = points[i];
        const previous = points[(i + points.length - 1) % points.length];
        const currentInside = isInside(current);
        const previousInside = isInside(previous);
        if (currentInside !== previousInside) {
          clipped.push(intersect(previous, current));
        }
        if (currentInside) {
          clipped.push(current);
        }
      }
      return clipped;
    }

    function intersectSegmentWithX(a, b, x) {
      const denominator = b.x - a.x;
      if (Math.abs(denominator) < 0.001) {
        return { x, y: a.y };
      }
      const t = (x - a.x) / denominator;
      return {
        x,
        y: a.y + (b.y - a.y) * t
      };
    }

