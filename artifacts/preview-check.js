    function upperClampBandPoints(leftX) {
      const rad = (180 - collisionBoundary.topBoundaryAngle) * Math.PI / 180;
      const ux = Math.cos(rad);
      const uy = Math.sin(rad);
      const nx = -uy;
      const ny = ux;
      const endAlong = leftX / ux;
      return [
        { x: 0, y: 0 },
        { x: endAlong * ux, y: endAlong * uy },
        {
          x: endAlong * ux + collisionBoundary.upperClampThickness * nx,
          y: endAlong * uy + collisionBoundary.upperClampThickness * ny
        },
        {
          x: collisionBoundary.upperClampThickness * nx,
          y: collisionBoundary.upperClampThickness * ny
        }
      ];
    }

