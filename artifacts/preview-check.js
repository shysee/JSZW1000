    function drawPolygon(points, fill, stroke) {
      ctx.save();
      ctx.beginPath();
      points.forEach((point, index) => {
        if (index === 0) {
          ctx.moveTo(point.x, point.y);
        } else {
          ctx.lineTo(point.x, point.y);
        }
      });
      ctx.closePath();
      ctx.fillStyle = fill;
      ctx.strokeStyle = stroke;
      ctx.lineWidth = 1.4;
      ctx.fill();
      ctx.stroke();
      ctx.restore();
    }

    function clipPolygonAboveYZero(points) {
      const clipped = [];
      for (let i = 0; i < points.length; i += 1) {
        const current = points[i];
        const previous = points[(i + points.length - 1) % points.length];
        const currentInside = current.y >= -0.001;
        const previousInside = previous.y >= -0.001;
        if (currentInside !== previousInside) {
          clipped.push(intersectSegmentWithY(previous, current, 0));
        }
        if (currentInside) {
          clipped.push(current);
        }
      }
      return clipped;
    }

    function intersectSegmentWithY(a, b, y) {
      const denominator = b.y - a.y;
      if (Math.abs(denominator) < 0.001) {
        return { x: a.x, y };
      }
      const t = (y - a.y) / denominator;
      return {
        x: a.x + (b.x - a.x) * t,
        y
      };
    }

